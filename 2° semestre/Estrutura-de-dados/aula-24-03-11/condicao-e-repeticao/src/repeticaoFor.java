/******************************************************************************
- estrutura de repetição for, é igual ao do JS
*******************************************************************************/
public class repeticaoFor
{
	public static void main(String[] args) {
        for(int conta = 1; conta <= 100; conta += 1){
            System.out.println(conta + ": hello world!");
        }
        System.out.println("A contagem terminou!");
	}
}
