/******************************************************************************
 - estrutura de repetição (while)
*******************************************************************************/

public class repeticaoWhile
{
	public static void main(String[] args) {
        int conta = 1;
        while (conta <=100){
            System.out.println(conta + ": Hello world!");
            conta += 1;
        }
        System.out.println("A contagem terminou!");
	}
}
